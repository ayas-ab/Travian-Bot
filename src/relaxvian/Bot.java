    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        try {
            if (buildingQueues.getSelectedRows().length != -1 && !GameQueue.getStat()) {
                DefaultTableModel model = (DefaultTableModel) this.buildingQueues.getModel();
                GameQueue.getVillage(currentVillage).getBuilds().remove(buildingQueues.getSelectedRow());
                model.removeRow(buildingQueues.getSelectedRow());
                JOptionPane.showMessageDialog(rootPane, "Field removed !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Please stop the bot first to delete.");

        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

