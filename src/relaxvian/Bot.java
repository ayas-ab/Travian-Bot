    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (this.TroopAmmount.getText().length() > 0 && this.FarmID.getText().length()
                > 0 && !this.unitType.getSelectedItem().equals("Choose unit type")
                && !this.sendTime.getSelectedItem().equals("Send every") && !this.unitType.getSelectedItem().equals("-")) {

            try {
                int sendingTime = Integer.parseInt(this.sendTime.getSelectedItem().toString().replaceAll(" min", ""));
                int troopType = this.unitType.getSelectedIndex();
                int zaAmmount = Integer.parseInt(this.TroopAmmount.getText());
                if (this.FarmID.getText().contains(",")){
                    String[] stripper = FarmID.getText().split(",");
                    for (int i = 0; i < stripper.length; i++){
                      GameQueue.AddFarm(new Farm(Integer.parseInt(stripper[i]), zaAmmount, troopType, sendingTime, currentVillage), this.currentVillage);
                                     Bot.log("Farm " + stripper[i] + " added to farm list, raided every " + sendingTime + " mins");

                    }
                }else{
                    int villid = Integer.parseInt(this.FarmID.getText());
                GameQueue.AddFarm(new Farm(villid, zaAmmount, troopType, sendingTime, currentVillage), this.currentVillage);
                                Bot.log("Farm " + villid + " added to farm list, raided every " + sendingTime + " mins");

                }

                JOptionPane.showMessageDialog(rootPane, "Farm added");
            } catch (Exception e) {

                JOptionPane.showMessageDialog(rootPane, "There is an error in your input");

            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "There is an error in your input");

        }


    }//GEN-LAST:event_jButton3ActionPerformed

